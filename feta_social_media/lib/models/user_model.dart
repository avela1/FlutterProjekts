class User {
  final String id;
  final String username;
  final String fullName;
  final String email;
  final String profileImgUrl;
  final String coverImgUrl;

  User( {
    required this.id,
    required this.username,
    required this.fullName,
    required this.email,
    required this.profileImgUrl,
    required this.coverImgUrl,
  });
}
